package com.robomech.geocoding.model;

public class Location {
    private double lat;
    private double lng;

    // Getter for latitude
    public double getLat() {
        return lat;
    }

    // Setter for latitude
    public void setLat(double lat) {
        this.lat = lat;
    }

    // Getter for longitude
    public double getLng() {
        return lng;
    }

    // Setter for longitude
    public void setLng(double lng) {
        this.lng = lng;
    }
}
